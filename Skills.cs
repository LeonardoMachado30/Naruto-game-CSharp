using System;

class Skills
{
    public int chakara = 0;
    public int velocidade = 0;
    public int forca = 0;
    public int area = 0;

    public Skills(int chakara = 0, int velocidade = 0, int forca = 0, int area = 0)
    {
        this.chakara = chakara;
        this.velocidade = velocidade;
        this.forca = forca;
        this.area = area;
    }
}

